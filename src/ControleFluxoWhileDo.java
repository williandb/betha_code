public class ControleFluxoWhileDo {
    public static void main(String[] args){
        //Exemplo While
        int x = 10;
        while(x < 10){
            System.out.println("Item X " + x);
            x++;
        }

        //Exemplo Do While
        int y = 10;
        do{
            System.out.println("Item Y "+y);
            y++;
        } while (y < 10);
    }
}
